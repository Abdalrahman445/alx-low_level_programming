#include "3-calc.h"
/**
 * op_add - a function to add two numbers
 * @a: the first number
 * @b: the second number
 * Return: the sum of them.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - a function to substract
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - a function to multiply two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - a function that make a division
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - a function to make a mod
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_mod(int a, int b)
{
return (a % b);
}

